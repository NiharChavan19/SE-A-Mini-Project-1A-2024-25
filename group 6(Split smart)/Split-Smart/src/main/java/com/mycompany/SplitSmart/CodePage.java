    private void joinGroup(String username) {
        String enteredCode = codeField.getText();
        System.out.println("CODE"+ enteredCode);
        // Check if the code exists and update the userâ€™s group code
        if (enteredCode != null && !enteredCode.isEmpty()) {
            try (Connection conn = DatabaseConnection.getConnection();
                 PreparedStatement ps = conn.prepareStatement("SELECT * FROM users WHERE code = ?")) {
                
                ps.setString(1, enteredCode);
                ResultSet rs = ps.executeQuery();
                
                if (rs.next()) {

                    try (PreparedStatement updatePs = conn.prepareStatement("UPDATE users SET group_code = ? WHERE username = ?")) {
                        updatePs.setString(1, enteredCode);
                        updatePs.setString(2, username);
                        updatePs.executeUpdate();
                        UserSession.getInstance().setGroupCode(enteredCode);
                        JOptionPane.showMessageDialog(this, "Successfully joined the group!");
                        SettleUp SettleUpFrame = new SettleUp();
                        SettleUpFrame.setVisible(true);
                        SettleUpFrame.pack();
                        SettleUpFrame.setLocationRelativeTo(null);
                        this.dispose();   
                    }

                } else {
                    JOptionPane.showMessageDialog(this, "Invalid group code. Please try again.");
                }
            } catch (SQLException e) {
                e.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please enter a group code.");
        }
    }

