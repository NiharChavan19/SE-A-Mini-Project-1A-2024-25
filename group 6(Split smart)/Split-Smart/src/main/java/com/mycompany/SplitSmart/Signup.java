    private void SignupBtnSignupageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SignupBtnSignupageActionPerformed
        String username = usernameField.getText();
        String password = new String(passwordField.getPassword());
        String confirmPassword = new String(confirmPasswordField.getPassword());
        if (!password.equals(confirmPassword)) {
            JOptionPane.showMessageDialog(this, "Passwords do not match!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        } 
        String code = generateRandomCode();
        registerNewUser(username, password, code);
         
    }//GEN-LAST:event_SignupBtnSignupageActionPerformed

