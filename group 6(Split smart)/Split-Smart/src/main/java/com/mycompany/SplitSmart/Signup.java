    private void registerNewUser(String username, String password, String code) {
        // Save the new user into the database
        try (Connection conn = DatabaseConnection.getConnection()) {
            String query = "INSERT INTO users (username, password, code, group_code) VALUES (?, ?, ?, ?)";
            PreparedStatement ps = conn.prepareStatement(query);
            ps.setString(1, username);
            ps.setString(2, password);
            ps.setString(3, code);
            ps.setString(4, code);
            ps.executeUpdate();

            JOptionPane.showMessageDialog(this, "Signup successful!", "Success", JOptionPane.INFORMATION_MESSAGE);
            UserSession.getInstance().setUsername(username);
            UserSession.getInstance().setCode(code);
            MainPage MainPageFrame = new MainPage();
            MainPageFrame.setVisible(true);
            MainPageFrame.pack();
            MainPageFrame.setLocationRelativeTo(null);
            this.dispose();  
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error during signup. Please try again.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private String generateRandomCode() {
        String ALPHANUMERIC_STRING = "ABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789";
        int CODE_LENGTH = 5;
        SecureRandom random = new SecureRandom();
        StringBuilder code = new StringBuilder(CODE_LENGTH);
        for (int i = 0; i < CODE_LENGTH; i++) {
            int index = random.nextInt(ALPHANUMERIC_STRING.length());
            code.append(ALPHANUMERIC_STRING.charAt(index));
        }
        return code.toString();
    }
    
 
